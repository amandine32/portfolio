        <h2 class="featurette-heading">La réalité augmentée <span class="text-muted">
        </span></h2>
        <br>  <br>  <br>  <br>
        <p class="lead">La réalité augmentée correspond à une interface virtuelle, en 2D ou en 3D, qui enrichit la
réalité en y superposant des informations complémentaires.
La réalité virtuelle consiste à filmer le monde réel en y ajoutant en direct des objets virtuels,
animations, textes, données, sons, que l'utilisateur visualise sur l'écran. Cela peut-être un
smartphone, une tablette tactile, une paire de lunettes ou un casque.
La première version « mobile » de réalité augmentée fut développée à la fin des années 90
et nécessitait un sac à dos connecté à un écran. L'ensemble était lourd, encombrant et
limitait drastiquement les capacités de visualisation. Il s'agissait plus d'un projet
expérimental qu'un réel outil grand public.
Donc les domaines d'applications touchent aussi bien les loisirs (avec les jeux vidéo) que
l'éducation, la médecine, mais aussi l'industrie.
Autrefois, la technologie de réalité augmentée était réservée à quelques secteurs comme
l'aviation, la défense militaire ou l'industrie. C'est en 2010 que la réalité augmentée fait son
apparition dans la publicité. De grandes entreprises comme Disney, Coca Cola ou Pepsi ont
utilisé la réalité augmentée pour créer des opérations commerciales.
En 2012, Google a lancé le test des Google Glass, le premier produit AR à être largement
commercialisé et rendant cette technologie accessible à tous. Les Google Glass n'ont pas
eu de succès et Google a rapidement stoppé la production de ces lunettes début 2015. En
2019, les Google Glass sont de retour mais uniquement pour les professionnels.
Pour finir, nous allons parler de la révolution Pokemon Go ! Sortie en 2016, l'application
mobile a placé la réalité augmentée au cœur de son gameplay. Les joueurs sont dans un
environnement réel, avec leurs téléphones, afin d'attraper des Pokémon qui relèvent du
virtuel. La réalité augmentée rend cela possible.
